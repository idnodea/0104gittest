//변수 선언
package sec01;

public class Variable {
	public static void main(String[] args) {
	
		//변수 value 선언
		int value = 0;  //int value;   이런식으로 선언x
		
		//변수 value 값을 읽고 10을 더하는 산술 연산을 수행
		//연산의 결과값을 변수 result의 초기값으로 저장
		
		int result = value +10;
		
		//변수 esult 값을 일고 콘솔에 출력
		System.out.println(result);
	}
	
}
